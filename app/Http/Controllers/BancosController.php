     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'descricao' => 'required|string'
        ]);

        Banco::create($request->all());

        return redirect()
        ->route('bancos.index')
        ->with('success','Banco adicionado com sucesso.');
    }

